        if ($type == 'single') {
            return $this->assetPartsId;
        } else {
            if ($type == 'array') {
                return $this->assetPartsId [$key];
            } else {
                echo json_encode(
                        array("success" => false, "message" => "Cannot Identify Type String Or Array:geAssetPartsId ?")
                );
                exit();
            }
        }
    }

