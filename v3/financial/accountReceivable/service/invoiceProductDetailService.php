            $d = 0;
            $invoiceProjectTitle = null;
            while (($row = $this->q->fetchArray($result)) == true) {
                if ($d != 0) {
                    if ($invoiceProjectTitle != $row['invoiceProjectTitle']) {
                        $str .= "</optgroup><optgroup label=\"" . $row['invoiceProjectTitle'] . "\">";
